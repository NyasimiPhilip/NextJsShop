const HandleComponent = () => {
    return (
      <div className='w-5 h-5 rounded-full shadow border bg-white border-zinc-200 transition hover:bg-primary' />
    )
  }
  
  export default HandleComponent

  /**
 * ClassNames Explanation:
 * 
 * - 'w-5': Sets the width to 1.25rem (20px).
 * - 'h-5': Sets the height to 1.25rem (20px).
 * - 'rounded-full': Rounds the corners to make the element a circle.
 * - 'shadow': Adds a default box shadow.
 * - 'border': Adds a border with default width (1px).
 * - 'bg-white': Sets the background color to white.
 * - 'border-zinc-200': Sets the border color to zinc-200, a light gray color.
 * - 'transition': Applies a smooth transition effect to any changes in properties.
 * - 'hover:bg-primary': Changes the background color to the primary color on hover.
 */
